void solve() {

    int n, m;
    cin >> n >> m;
    string arr[n];
    for (int i = 0; i < n; i++) {
        cin >> arr[i];
    }
    int ans = 1;
    for (int j = 0; j < m; j++) {
        bool yes = true;
        for (int i = 0; i < n; i++) {
            if(arr[i][j] != '_')
                yes = false;
        }
        ans += yes;
    }    
    cout << ans;
    return;
}

int main(){
    fast;
    int TC = 1;  // cin >> TC;
    for(int i = 1; i <= TC; i++){
        solve();
    }
}

