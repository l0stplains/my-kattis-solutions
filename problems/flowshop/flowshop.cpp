const int INF = numeric_limits<int>::max();

void solve() {

    int n, m;
    cin >> n >> m;
    int arr[n + 1][m + 1] = {};
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) {
            cin >> arr[i][j];
        }
    }
    for (int j = 1; j <= m; j++) {
        for (int i = 1; i <= n; i++) {
            arr[i][j] += max(arr[i - 1][j], arr[i][j - 1]);
        }
    }

    for (int i = 1; i <= n; i++) {
        cout << arr[i][m] << " ";
    }



    return;
}

int main(){
    fast;
    int TC = 1;  // cin >> TC;
    for(int i = 1; i <= TC; i++){
        solve();
    }
}

